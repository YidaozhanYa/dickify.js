const exportGlobalVariables = () => {
    const windowProps = Object.getOwnPropertyNames(window)
    let globalVariables = []
    windowProps.forEach(prop => {
        if (!(
            prop.startsWith('_') || prop.startsWith('on') || ['window', 'self', 'name', 'exportGlobalVariables'].includes(prop)
        ) && (
            prop.length > 1
        )) {
            globalVariables.push(prop)
        }
    })
    return JSON.stringify(globalVariables)
}

